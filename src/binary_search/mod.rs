pub fn maximum_count(nums: Vec<i32>) -> i32 {
    let mut left: i32 = 0;
    let mut right: i32 = nums.len() as i32 - 1;
    while left <= right {
        let mid = left + (right - left) / 2;
        if nums[mid as usize] >= 0 {
            right = mid - 1;
        } else {
            left = mid + 1;
        }
    }
    right = left;
    while right < nums.len() as i32 && nums[right as usize] == 0 {
        right += 1;
    }
    left.max(nums.len() as i32 - right)
}

